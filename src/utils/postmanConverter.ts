export function collectionToHttpFileContent(collection: PostmanCollection): string {
    const sections: string[] = [];

    const variables = (collection.variable ?? []).filter(v => !v.disabled);
    if (variables.length) {
        sections.push(variables.map(v => `@${v.key} = ${v.value}`).join('\n'));
    }

    const leaves: { name: string; request: PostmanRequest }[] = [];
    collectLeafRequests(collection.item ?? [], [], leaves);

    for (const { name, request } of leaves) {
        sections.push(`### ${name}\n${buildHttpBlock(name, request)}`);
    }

    return sections.join('\n\n') + '\n';
}

function collectLeafRequests(
    items: PostmanItem[],
    folderPath: string[],
    out: { name: string; request: PostmanRequest }[]
): void {
    for (const item of items) {
        if (item.item?.length) {
            collectLeafRequests(item.item, [...folderPath, item.name], out);
        } else if (item.request) {
            out.push({ name: [...folderPath, item.name].join(' / '), request: item.request });
        }
    }
}

function buildHttpBlock(name: string, request: PostmanRequest): string {
    const lines: string[] = [`# @name ${name}`];

    const { headerLines: authHeaderLines, queryParams } = buildAuthArtifacts(request.auth);

    let url = typeof request.url === 'string' ? request.url : (request.url.raw || buildUrlFromParts(request.url));
    if (queryParams.length) {
        const separator = url.includes('?') ? '&' : '?';
        url += separator + queryParams.map(q => `${q.key}=${q.value}`).join('&');
    }

    lines.push(`${(request.method || 'GET').toUpperCase()} ${url}`);

    const seenHeaderNames = new Set<string>();
    const headerLines: string[] = [];
    for (const line of authHeaderLines) {
        if (!line.startsWith('#')) {
            seenHeaderNames.add(line.slice(0, line.indexOf(':')).trim().toLowerCase());
        }
        headerLines.push(line);
    }

    for (const h of request.header ?? []) {
        if (h.disabled || seenHeaderNames.has(h.key.toLowerCase())) {
            continue;
        }
        seenHeaderNames.add(h.key.toLowerCase());
        headerLines.push(`${h.key}: ${h.value}`);
    }

    const { body, extraHeaderLines } = buildHttpBody(request.body);
    for (const line of extraHeaderLines) {
        const headerName = line.slice(0, line.indexOf(':')).trim().toLowerCase();
        if (!seenHeaderNames.has(headerName)) {
            seenHeaderNames.add(headerName);
            headerLines.push(line);
        }
    }

    lines.push(...headerLines);

    if (body !== undefined) {
        lines.push('');
        lines.push(body);
    }

    return lines.join('\n');
}

function buildUrlFromParts(url: PostmanUrl): string {
    const protocol = url.protocol ? `${url.protocol}://` : '';
    const host = (url.host ?? []).join('.');
    const pathPart = (url.path ?? []).join('/');
    const query = (url.query ?? []).filter(q => !q.disabled).map(q => `${q.key}=${q.value}`).join('&');
    return `${protocol}${host}${pathPart ? '/' + pathPart : ''}${query ? '?' + query : ''}`;
}

function buildAuthArtifacts(auth: PostmanAuth | undefined): { headerLines: string[]; queryParams: PostmanUrlQueryParam[] } {
    const headerLines: string[] = [];
    const queryParams: PostmanUrlQueryParam[] = [];

    if (!auth || auth.type === 'noauth') {
        return { headerLines, queryParams };
    }

    switch (auth.type) {
        case 'bearer': {
            const token = auth.bearer?.find(p => p.key === 'token')?.value ?? auth.bearer?.[0]?.value ?? '';
            headerLines.push(`Authorization: Bearer ${token}`);
            break;
        }
        case 'basic': {
            const username = auth.basic?.find(p => p.key === 'username')?.value ?? '';
            const password = auth.basic?.find(p => p.key === 'password')?.value ?? '';
            if (username.includes('{{') || password.includes('{{')) {
                headerLines.push(`# TODO: set Basic auth manually (username=${username}, password=${password})`);
            } else {
                headerLines.push(`Authorization: Basic ${Buffer.from(`${username}:${password}`).toString('base64')}`);
            }
            break;
        }
        case 'apikey': {
            const key = auth.apikey?.find(p => p.key === 'key')?.value ?? '';
            const value = auth.apikey?.find(p => p.key === 'value')?.value ?? '';
            const location = auth.apikey?.find(p => p.key === 'in')?.value ?? 'header';
            if (location === 'query') {
                queryParams.push({ key, value });
            } else {
                headerLines.push(`${key}: ${value}`);
            }
            break;
        }
        default:
            headerLines.push(`# TODO: auth type '${auth.type}' is not supported for automatic conversion, configure manually`);
    }

    return { headerLines, queryParams };
}

function buildHttpBody(body?: PostmanBody): { body?: string; extraHeaderLines: string[] } {
    if (!body?.mode) {
        return { extraHeaderLines: [] };
    }

    switch (body.mode) {
        case 'raw': {
            const language = body.options?.raw?.language;
            const contentType = language ? rawLanguageToContentType[language] : undefined;
            return { body: body.raw ?? '', extraHeaderLines: contentType ? [`Content-Type: ${contentType}`] : [] };
        }
        case 'urlencoded': {
            const pairs = (body.urlencoded ?? []).filter(p => !p.disabled).map(p => `${p.key}=${p.value}`);
            return { body: pairs.join('&'), extraHeaderLines: ['Content-Type: application/x-www-form-urlencoded'] };
        }
        case 'graphql': {
            const payload = {
                query: body.graphql?.query ?? '',
                variables: body.graphql?.variables ? JSON.parse(body.graphql.variables) : {}
            };
            return { body: JSON.stringify(payload, null, 2), extraHeaderLines: ['X-Request-Type: GraphQL'] };
        }
        case 'formdata': {
            const { body: multipartBody, boundary } = buildMultipartBody(body.formdata ?? []);
            return { body: multipartBody, extraHeaderLines: [`Content-Type: multipart/form-data; boundary=${boundary}`] };
        }
        default:
            return { extraHeaderLines: [] };
    }
}

function buildMultipartBody(params: PostmanFormParam[]): { body: string; boundary: string } {
    const boundary = `----RestClientBoundary${Date.now()}`;
    const parts: string[] = [];

    for (const param of params) {
        if (param.disabled) {
            continue;
        }

        if (param.type === 'file') {
            const fileName = param.src ? path.basename(param.src) : 'file';
            parts.push(
                `--${boundary}\r\nContent-Disposition: form-data; name="${param.key}"; filename="${fileName}"\r\n\r\n` +
                `# TODO: replace with "< /local/path/to/file" -- the original Postman file path is not portable`
            );
        } else {
            parts.push(`--${boundary}\r\nContent-Disposition: form-data; name="${param.key}"\r\n\r\n${param.value ?? ''}`);
        }
    }

    parts.push(`--${boundary}--`);
    return { body: parts.join('\r\n'), boundary };
}

