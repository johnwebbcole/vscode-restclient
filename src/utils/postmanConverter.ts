const rawLanguageToContentType: Record<string, string> = {
    json: 'application/json',
    xml: 'application/xml',
    html: 'text/html',
    javascript: 'application/javascript',
    text: 'text/plain'
};

export function isPostmanCollection(json: any): json is PostmanCollection {
    return !!json && typeof json === 'object' && typeof json.info === 'object' && Array.isArray(json.item);
}

export function isPostmanEnvironment(json: any): json is PostmanEnvironment {
    return !!json
        && typeof json === 'object'
        && typeof json.name === 'string'
        && Array.isArray(json.values)
        && !Array.isArray(json.item);
}

