export function environmentToVariableMap(env: PostmanEnvironment): { [key: string]: string } {
    const map: { [key: string]: string } = {};
    for (const value of env.values) {
        if (value.enabled !== false) {
            map[value.key] = value.value;
        }
    }
    return map;
}

export function buildPostmanEnvironment(name: string, variables: { [key: string]: string }): PostmanEnvironment {
    return {
        name,
        values: Object.entries(variables).map(([key, value]) => ({ key, value, enabled: true, type: 'default' })),
        _postman_variable_scope: 'environment'
    };
}
