export function buildCollectionFromHttpRequests(
    name: string,
    requests: { name: string; request: HttpRequest }[],
    variables: PostmanVariable[] = []
): PostmanCollection {
    const referencesBySourceRequest = collectRequestVariableReferences(requests);

    const item: PostmanItem[] = requests.map(({ name: itemName, request }) => {
        const postmanItem: PostmanItem = {
            name: itemName,
            request: buildPostmanRequest(request)
        };

        const references = referencesBySourceRequest.get(itemName);
        if (references?.size) {
            postmanItem.event = [{
                listen: 'test',
                script: { type: 'text/javascript', exec: buildRequestVariableTestScript(references) }
            }];
        }

        return postmanItem;
    });

    return {
        info: { name, schema: PostmanSchemaV21 },
        item,
        ...(variables.length ? { variable: variables } : {})
    };
}

// REST Client lets one request reference a prior request's response, e.g.
// {{alerts_active.response.body.$.features[0].properties.id}}. Postman has no
// equivalent syntax, but Postman variable names may contain dots/brackets, so
// the same literal expression can be kept as a collection variable's key --
// populated by a generated post-response "test" script on the SOURCE request
// (here, alerts_active) -- and every other request that already references
// {{alerts_active.response...}} keeps working unmodified.
const requestVariableReferenceRegex = /\{\{(\w+\.response\.(?:body|headers)(?:\.[^}]*)?)\}\}/g;
const requestVariableExpressionRegex = /^(\w+)\.response\.(body|headers)(?:\.(.*))?$/;

function collectRequestVariableReferences(requests: { name: string; request: HttpRequest }[]): Map<string, Set<string>> {
    const referencesBySourceRequest = new Map<string, Set<string>>();

    for (const { request } of requests) {
        const haystacks = [
            request.url,
            ...Object.values(request.headers).map(value => value?.toString() ?? ''),
            request.rawBody ?? ''
        ];

        for (const text of haystacks) {
            requestVariableReferenceRegex.lastIndex = 0;
            let match: RegExpExecArray | null;
            while ((match = requestVariableReferenceRegex.exec(text)) !== null) {
                const expression = match[1];
                const sourceMatch = requestVariableExpressionRegex.exec(expression);
                if (!sourceMatch) {
                    continue;
                }

                const sourceRequestName = sourceMatch[1];
                let references = referencesBySourceRequest.get(sourceRequestName);
                if (!references) {
                    references = new Set();
                    referencesBySourceRequest.set(sourceRequestName, references);
                }
                references.add(expression);
            }
        }
    }

    return referencesBySourceRequest;
}

function buildRequestVariableTestScript(expressions: Set<string>): string[] {
    const lines: string[] = [
        '// Auto-generated by REST Client export: populates {{name.response...}} variables referenced by other requests.'
    ];

    for (const expression of expressions) {
        const match = requestVariableExpressionRegex.exec(expression);
        if (!match) {
            continue;
        }

        const [, , part, rest] = match;
        lines.push('try {');

        if (part === 'headers') {
            if (rest) {
                lines.push(`    pm.collectionVariables.set(${JSON.stringify(expression)}, pm.response.headers.get(${JSON.stringify(rest)}));`);
            }
        } else if (!rest || rest === '*') {
            lines.push(`    pm.collectionVariables.set(${JSON.stringify(expression)}, pm.response.text());`);
        } else {
            const lodashPath = jsonPathToLodashPath(rest);
            if (lodashPath === null) {
                lines.push(`    // TODO: "${rest}" uses JSONPath syntax that can't be auto-translated (wildcards, filters, "..", or the backtick "@name" escape). Extract it manually.`);
            } else {
                lines.push('    const __body = pm.response.json();');
                lines.push(`    const __value = _.get(__body, ${JSON.stringify(lodashPath)});`);
                lines.push(`    if (__value !== undefined) { pm.collectionVariables.set(${JSON.stringify(expression)}, typeof __value === 'string' ? __value : JSON.stringify(__value)); }`);
            }
        }

        lines.push(`} catch (e) { /* leave ${JSON.stringify(expression)} unset on failure */ }`);
    }

    return lines;
}

function jsonPathToLodashPath(rawPath: string): string | null {
    let jsonPath = rawPath;
    if (jsonPath.startsWith('asJson.')) {
        jsonPath = jsonPath.slice('asJson.'.length);
    } else if (jsonPath.startsWith('asXml.')) {
        return null;
    }

    if (jsonPath.startsWith('$.')) {
        jsonPath = jsonPath.slice(2);
    } else if (jsonPath === '$') {
        jsonPath = '';
    }

    if (!jsonPath || /`|\.\.|\*|\[\?\(|@\./.test(jsonPath)) {
        return null;
    }

    return jsonPath;
}

function buildPostmanRequest(request: HttpRequest): PostmanRequest {
    const header = buildPostmanHeaders(request.headers);
    const body = buildPostmanBody(request, header);

    return {
        method: request.method,
        header,
        url: buildPostmanUrl(request.url),
        ...(body ? { body } : {})
    };
}

function buildPostmanHeaders(headers: HttpRequest['headers']): PostmanHeader[] {
    const result: PostmanHeader[] = [];
    for (const key in headers) {
        const value = headers[key];
        if (value === undefined) {
            continue;
        }

        const values = Array.isArray(value) ? value : [value.toString()];
        for (const v of values) {
            result.push({ key, value: v });
        }
    }
    return result;
}

function buildPostmanUrl(rawUrl: string): PostmanUrl {
    // Parsed by hand rather than with the WHATWG URL class, since a templated
    // authority (e.g. {{baseUrl}}/path) isn't a valid absolute URI and would
    // throw. Postman's own editor needs the host/path/query breakdown -- not
    // just `raw` -- to reliably populate the address bar on import, including
    // for templated base URLs (where the whole "{{baseUrl}}" token becomes a
    // single host segment).
    const url: PostmanUrl = { raw: rawUrl };

    let remainder = rawUrl;

    const protocolMatch = /^([a-zA-Z][a-zA-Z0-9+.-]*):\/\//.exec(remainder);
    if (protocolMatch) {
        url.protocol = protocolMatch[1];
        remainder = remainder.slice(protocolMatch[0].length);
    }

    const queryIndex = remainder.indexOf('?');
    let queryString = '';
    if (queryIndex !== -1) {
        queryString = remainder.slice(queryIndex + 1);
        remainder = remainder.slice(0, queryIndex);
    }

    const slashIndex = remainder.indexOf('/');
    const hostPart = slashIndex === -1 ? remainder : remainder.slice(0, slashIndex);
    const pathPart = slashIndex === -1 ? '' : remainder.slice(slashIndex + 1);

    url.host = hostPart.includes('{{') ? [hostPart] : hostPart.split('.').filter(segment => segment.length > 0);

    if (pathPart) {
        url.path = pathPart.split('/').filter(segment => segment.length > 0);
    }

    if (queryString) {
        url.query = queryString.split('&').filter(pair => pair.length > 0).map(pair => {
            const [key, ...rest] = pair.split('=');
            return { key: decodeURIComponent(key), value: decodeURIComponent(rest.join('=')) };
        });
    }

    return url;
}

function buildPostmanBody(request: HttpRequest, headers: PostmanHeader[]): PostmanBody | undefined {
    const rawBody = request.rawBody;
    if (!rawBody) {
        return undefined;
    }

    const contentType = getContentType(request.headers);

    if (MimeUtility.isFormUrlEncoded(contentType)) {
        const urlencoded: PostmanUrlQueryParam[] = rawBody.split('&').filter(pair => pair.length > 0).map(pair => {
            const [key, ...rest] = pair.split('=');
            return { key: decodeURIComponent(key), value: decodeURIComponent(rest.join('=')) };
        });
        return { mode: 'urlencoded', urlencoded };
    }

    if (MimeUtility.isMultiPartFormData(contentType)) {
        // No robust multipart parser -- pass the literal body through as raw text.
        return { mode: 'raw', raw: rawBody };
    }

    let language = 'text';
    if (MimeUtility.isJSON(contentType)) {
        language = 'json';
    } else if (MimeUtility.isXml(contentType)) {
        language = 'xml';
    } else if (MimeUtility.isHtml(contentType)) {
        language = 'html';
    } else if (MimeUtility.isJavaScript(contentType)) {
        language = 'javascript';
    }

    return { mode: 'raw', raw: rawBody, options: { raw: { language } } };
}

